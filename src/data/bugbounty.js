    'Fixed Confirmed':   {label:'Fixed ✓',       cls:'pd', color:'var(--success)'},
    'In Progress':       {label:'Em Progresso',   cls:'pp', color:'var(--accent)'},
    'Ticket Created':    {label:'Ticket Criado',  cls:'pi', color:'var(--accent)'},
    'Triage':            {label:'Em Triagem',     cls:'ph', color:'var(--high)'},
    'Awaiting Retest':   {label:'Aguard. Retest', cls:'ph', color:'var(--high)'},
