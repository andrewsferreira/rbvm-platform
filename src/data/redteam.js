export function computeRTScore(f) {
  let s = f.severity==='CRITICAL'?60:f.severity==='HIGH'?45:f.severity==='MEDIUM'?30:15;
  if (f.crown_jewel_impacted) s += 15;
  if (f.lateral_movement)     s += 8;
  if (f.persistence_observed) s += 8;
  if (f.data_access_observed) s += 5;
  if (f.detection_status === 'Not Detected') s += 10;
  if (f.prevention_status === 'Not Prevented') s += 5;
  if (f.retest_status === 'Failed') s += 10;
  return Math.min(100, s);
}
